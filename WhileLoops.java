public class WhileLoops {
    public static void main(String[] args)
    {
        //Creating while loops
        int age = 24;
        while (age <= 30){
            System.out.println(age);
            age++;
        }
        //Creating Do/While loops
        int i = 0;
        do{
            System.out.println(i);
            i++;
        }while(i < 10);

        //Second Do/While loops
        int y = 5;
        do{
            System.out.println(y);
            y++;
        }while(y < 20);
        





    }
    
}
